#include<iostream>
using namespace std;

int main()
{
	cout << "The size of datatype char is " 
		 << sizeof(char) << " bytes" << endl;
	cout << "The size of datatype int is " 
		 << sizeof(int) << " bytes" << endl;	 
	cout << "The size of datatype float is " 
		 << sizeof(float) << " bytes" << endl;
	cout << "The size of datatype double is " 
		 << sizeof(double) << " bytes" << endl;
	
	return 0;
}
